    private void jBtnSelectPluginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSelectPluginActionPerformed
        String selectedName = (String) jCBSeleccionarPlugin.getSelectedItem();
        IProjectRepositoryPlugin selectedPlugin = pluginMap.get(selectedName);

        if (selectedPlugin != null) {
            HomeProject homeproject = new HomeProject(selectedPlugin);
            homeproject.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un plugin v√°lido.");
        }
    }//GEN-LAST:event_jBtnSelectPluginActionPerformed

    private HashMap<String, IProjectRepositoryPlugin> pluginMap = new HashMap<>();

    public void llenarPlugins(List<IProjectRepositoryPlugin> listaPlugins) {
        jCBSeleccionarPlugin.removeAllItems();
        pluginMap.clear(); // Limpiar el mapa antes de llenarlo

        for (IProjectRepositoryPlugin plugin : listaPlugins) {
            String nombrePlugin = plugin.getClass().getSimpleName();
            pluginMap.put(nombrePlugin, plugin);
            jCBSeleccionarPlugin.addItem(nombrePlugin);
        }
    }

